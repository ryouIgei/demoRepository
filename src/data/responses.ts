export function getBotResponse(message: string): string {
  const lower = message.toLowerCase()
  for (const [keyword, response] of Object.entries(responseMap)) {
    if (lower.includes(keyword.toLowerCase())) {
      return response
    }
  }
  return `「${message}」についてのご質問ありがとうございます。\n\n詳細については担当部門へお問い合わせいただくか、左のカテゴリからよくある質問をお選びください。\n\n📧 社内ヘルプデスク: helpdesk@company.com\n📞 内線: 1234`
}
