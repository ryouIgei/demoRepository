export function getAutoResponse(category: Category, text: string): string {
  const rules = categoryRules[category];
  for (const rule of rules) {
    if (rule.keywords.some((kw) => text.includes(kw))) {
      return rule.answer;
    }
  }
  return `ご質問ありがとうございます。担当チームに確認の上、折り返しご連絡いたします。緊急の場合はITヘルプデスク（内線: 1234）または hr@company.internal までお問い合わせください。`;
}
